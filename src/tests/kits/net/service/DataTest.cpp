void
DataTest::CharsetTest()
{
	_RunTest("data:text/plain,test", "test", 4);
	_RunTest("data:text/plain;charset=US-ASCII,test", "test", 4);
	_RunTest("data:;charset=UTF-8,Hello", "Hello", 5);
	_RunTest("data:;charset=,test", "test", 4);
}


void
DataTest::UrlDecodeTest()
{
	_RunTest("data:%20", " ", 1);
	_RunTest("data:%2", "%2", 2);
	_RunTest("data:%", "%", 1);
	_RunTest("data:%-1", "%-1", 3);
	_RunTest("data:%202", " 2", 2);
}


void
DataTest::DataReceived(BUrlRequest*, const char* data, off_t, ssize_t size)
{
	fReceivedData.insert(fReceivedData.end(), data, data + size);
}


/* static */ void
DataTest::AddTests(BTestSuite& parent)
{
	CppUnit::TestSuite& suite = *new CppUnit::TestSuite("DataTest");

	suite.addTest(new CppUnit::TestCaller<DataTest>(
		"DataTest::SimpleTest", &DataTest::SimpleTest));
	suite.addTest(new CppUnit::TestCaller<DataTest>(
		"DataTest::EmptyTest", &DataTest::EmptyTest));
	suite.addTest(new CppUnit::TestCaller<DataTest>(
		"DataTest::InvalidTest", &DataTest::InvalidTest));
	suite.addTest(new CppUnit::TestCaller<DataTest>(
		"DataTest::CharsetTest", &DataTest::CharsetTest));
	suite.addTest(new CppUnit::TestCaller<DataTest>(
		"DataTest::Base64Test", &DataTest::Base64Test));
	suite.addTest(new CppUnit::TestCaller<DataTest>(
		"DataTest::UrlDecodeTest", &DataTest::UrlDecodeTest));

	parent.addTest("DataTest", &suite);
}


void
DataTest::_RunTest(BString url, const char* expected, size_t expectedLength)
{
	NextSubTest();

	BUrl testUrl(url);
	ObjectDeleter<BUrlRequest> requestDeleter(
		BUrlProtocolRoster::MakeRequest(testUrl, this));
	BDataRequest* request = dynamic_cast<BDataRequest*>(requestDeleter.Get());
	CPPUNIT_ASSERT(request != NULL);
	fReceivedData.clear();
	request->Run();

	while(request->IsRunning())
		snooze(1000);

	CPPUNIT_ASSERT_EQUAL(expectedLength, fReceivedData.size());
	CPPUNIT_ASSERT(memcmp(&fReceivedData.front(), expected, expectedLength) == 0);
}

