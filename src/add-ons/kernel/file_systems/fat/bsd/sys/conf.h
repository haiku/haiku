#ifndef FAT_CONF_H
#define FAT_CONF_H


// Modified to support the Haiku FAT driver.

struct cdev {
	int si_refcount;
	struct mount* si_mountpt;
	char si_name[SPECNAMELEN + 1];

	// Members added for Haiku port
	char si_device[B_DEV_NAME_LENGTH];
	int si_fd;
	off_t si_mediasize;
	dev_t si_id;
	device_geometry* si_geometry;
		// cylinder_count, head_count, and sectors_per_track are not always populated when mounting
		// use si_medisize to access device size instead
};

/* Defined uid and gid values. */
#define UID_ROOT 0
#define GID_WHEEL 0


#endif // FAT_CONF_H
